package com.aaa.qy111mp.entity;

import lombok.Data;

import java.util.List;

@Data
public class LayUiTable {
    private int code;
    private String msg;
    private Integer count;
    private List<?> data;

    @Override
    public String toString() {
        return "LayUiTable{" +
                "code=" + code +
                ", msg='" + msg + '\'' +
                ", count=" + count +
                ", data=" + data +
                '}';
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }

    public List<?> getData() {
        return data;
    }

    public void setData(List<?> data) {
        this.data = data;
    }
}
